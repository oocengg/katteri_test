     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {   
        /// get user with role 2 (user) with last subscribe and subscribe.payment 
        $users = User::where('role', 2)->with(['subscribe' => function($query){
            $query->orderBy('created_at', 'desc')->with('payment');
        }])->get();

        /// filter subscribe for last subscribe
        foreach($users as $user){
            $user->subscribe = $user->subscribe->first();
        }

        // paginate user
        // $users = $users->paginate(10);
        return view('admin.views.user.index', ['users' => $users]);
    }

